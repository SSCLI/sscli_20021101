#include <palsuite.h>

int __cdecl main(int argc, char **argv)
{
    /*
     *  Initialize the PAL and return FAIL if this fails
     */
    if (0 != (PAL_Initialize(argc, argv)))
    {
        return FAIL;
    }

    /*should return 0*/
    exit(0);

    Fail ("Exit didn't actually stop execution.\n");

    return FAIL;
}





