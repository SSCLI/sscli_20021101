function getstring(i)
{

	if (i > 0x0FFF)
		return i.toString(16);
	else if (i > 0x00FF)
		return "0" + i.toString(16);
	else if (i > 0x000F)
		return "00" + i.toString(16);
	else 
		return "000" + i.toString(16);
}

